using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace RomanNumeralConverter.App
{
    public class NumberConverter
    {
        public string Convert (int number)
        {
            string result = "";
            var character = "I";

            if (number == 1)
            {
              return result = character; 
            }

            return result;
        }
    }
}
